### Step 1: Install Nerd Fonts (Hack)
# Go to https://github.com/ryanoasis/nerd-fonts
# Download Hack.zip
# Install Hack Regular Nerd Font Complete Mono Windows Compatible.ttf

### Step 2: Configure Windows Terminal
## Settings > Startup > Default terminal application
# Change to Windows Terminal
## Settings > Appearance
# Turn on Use acrylic material in the tab row
## Settings > Defaults > Appearance > Text
# Change Color scheme to One Half Dark
# Change Font face to Hack NF
## Settings > Defaults > Appearance > Transparency
# Turn on Enable acrylic material
# Background opacity 50%

### Step 3: Install PowerShell
## Microsoft Store > PowerShell

### Step 4: Change the default shell
## Settings > Startup > Default profile
# Change to PowerShell

### Step 5: Change the terminal background color
## Settings > Open JSON file
# Duplicate the "One Half Dark" theme and rename it
# Change "background": "#282C34" to "background": "#001B26"
## Settings > Defaults > Appearance > Text
# Change Color scheme to One Half Dark (modded)

### Step 6: Center on launch
## Settings > Open JSON file
# Add "centerOnLaunch": true